import math

print("absolute func:",abs(-10.2))
print("exponential func:",math.exp(6))
print("ceil func:", math.ceil(10.2))
print ("fabs:",math.fabs(-45))
print("floor:",math.floor(100.21))
print("natural log:",math.log(2))
print("log base 10:",math.log10(2))
print("Max func:",max(100,200,300))
print("Min func:", min(100,200,300))
print("Modf:", math.modf(100.34))
print("power:", math.pow(10,3))
print("roundof:",round(68.69495793474,4))
print("sqrt:",math.sqrt(9))
print("sine:", math.sin(180))
print("tan:",math.tan(90))
print("radians to degree:",math.degrees(math.pi/2))
print("degree to radians:",math.radians(math.pi))